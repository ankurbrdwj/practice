package com.ankur.interview.arrays;

public class Pair {
    private Integer x;
    private Integer y;

    public Pair(int i, int y) {
        this.x = i;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
